    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int random = 0;
        char []cod = {'0', '0', '0', '0', '0'};
        boolean v = false;

        for (int i = 0; i < 5; i++) {
            v = false;
            do {
                random = (int) (Math.random() * 122);
                if (random >= 48 && random <= 57) {
                    v = true;
                }
            } while (!v);
            cod[i] = (char) random;
        }
        String matricula = new String(cod);

        cntrInteracao.cadastrarNovoFuncionario(jTextField1.getText(),
                matricula, jTextField3.getText());
    }//GEN-LAST:event_jButton1ActionPerformed

